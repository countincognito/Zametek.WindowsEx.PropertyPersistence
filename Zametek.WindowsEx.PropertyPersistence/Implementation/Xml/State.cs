using System.Collections.Generic;
using System.Xml.Serialization;

namespace Zametek.WindowsEx.PropertyPersistence
{
    [XmlRoot("state")]
    public class State
        : IAmState<Element>
    {
        public State()
        {
            Elements = new List<Element>();
        }

        [XmlElement("element")]
        public List<Element> Elements
        {
            get;
            private set;
        }
    }
}
