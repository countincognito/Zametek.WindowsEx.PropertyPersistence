using System.Collections.Generic;
using System.Xml.Serialization;

namespace Zametek.WindowsEx.PropertyPersistence.Xml
{
    [XmlType("element")]
    public class Element
        : IAmElement<Property>
    {
        public Element()
        {
            Properties = new List<Property>();
        }

        [XmlElement("property")]
        public List<Property> Properties
        {
            get;
            private set;
        }

        [XmlAttribute("uid")]
        public string Uid
        {
            get;
            set;
        }
    }
}
